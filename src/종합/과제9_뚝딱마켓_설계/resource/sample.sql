#  01.중고물품 테이블
create table usedGoods(				  
	ugNo int unsigned auto_increment,
    constraint primary key(ugNo),    -- 1) 중고물품번호(pk)
    ugName varchar(50),			     -- 2) 중고물품명
    ugPrice int unsigned default 0,  -- 3) 가격
    ugSeller varchar(30) not null,   -- 4) 판매자(닉네임)
    ugPwd int unsigned not null,	 -- 5) 비밀번호
    ugStatus int unsigned default 0, -- 6) 판매여부( 판매중: 0 / 판매완료: 1 )
    ugDescription longtext,		     -- 7) 설명
    ugCreatedAt datetime default now()   -- 8) 등록일
);

