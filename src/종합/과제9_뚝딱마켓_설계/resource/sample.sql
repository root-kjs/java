# 02. 중고물품 > 문의댓글 테이블
create table comments(               
	cNo int unsigned auto_increment,   
    constraint primary key(cNo),    	-- 1) 문의댓글번호(pk)
	ugNo int unsigned,              	-- 2) 중고물품번호(fk)
    constraint foreign key(ugNo) references usedGoods(ugNo),
    cWriter varchar(30),            	-- 3) 작성자(닉네임)
    cInquiry longtext,          		-- 4) 문의내용
    cPwd int unsigned not null,    	 	-- 5) 비밀번호
    cCreatedAt datetime default now() 	-- 6) 작성일
);

